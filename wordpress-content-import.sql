<h3>Restaurant Chain Overtime Violations</h3>
<div class="case-summary">
<p><strong>Challenge:</strong> A multi-location restaurant chain allegedly failed to pay proper overtime to assistant managers. This complex class action involved 150+ affected employees.</p>
<p><strong>Our Approach:</strong> Analyzed payroll records, scheduling data, and job descriptions to calculate unpaid overtime using DOL methodology. Statistical sampling for efficiency.</p>
<p><strong>Damage Calculation Methodology:</strong></p>
<ul>
<li>Class member identification and eligibility determination</li>
<li>Statistical sampling methodology for payroll record analysis</li>
<li>Overtime calculation using Department of Labor standards</li>
<li>Back wage calculations with appropriate interest and liquidated damages</li>
</ul>
